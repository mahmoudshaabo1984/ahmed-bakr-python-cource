# Indexing - الفهرسة
# الفهرسة تتيح الوصول إلى عنصر محدد في النص بناءً على موقعه (من البداية أو النهاية):  

user = 'ahmedbakr'

# الحصول على أول حرف
print(user[0])  # 'a'

# الحصول على آخر حرف
print(user[-1])  # 'r'

# الحصول على الحرف الثاني
print(user[1])  # 'h'

# الحصول على الحرف الرابع من النهاية
print(user[-6])  # 'e'


# Slicing - التقطيع
# التقطيع يتيح استخراج أجزاء من النص باستخدام النطاق (start:stop:step):  

user = 'ahmedbakr'

# الحصول على الحروف بدءًا من الموقع الخامس حتى النهاية
print(user[5:])  # 'bakr'

# الحصول على أول أربعة أحرف
print(user[:4])  # 'ahme'

# الحصول على الحروف بين الموقع الثاني والسابع
print(user[2:7])  # 'medba'

# الحصول على النص بالكامل بالعكس (التقطيع بالعكس)
print(user[::-1])  # 'rkabdemha'

# الحصول على كل حرفين
print(user[::2])  # 'amdbk'

# أمثلة إضافية
# استخراج آخر ثلاثة أحرف
print(user[-3:])  # 'akr'

# استخراج النص بدون أول وآخر حرف
print(user[1:-1])  # 'hmedbak'

# عكس النص جزئيًا:

# عكس أول خمسة أحرف فقط
print(user[4::-1])  # 'demha'

# عكس الحروف بين الموقع الثاني والسادس
print(user[6:2:-1])  # 'edba'

# استخراج كل حرف ثاني
print(user[::2])  # 'amdbk'

# استخراج النص بالعكس، ولكن كل حرفين
print(user[::-2])  # 'rbdm'