# أمثلة على Boolean Operators
print("\nأمثلة على Boolean Operators:")
x, y = True, False
print("x:", x, "y:", y)
print("x and y:", x and y)  # التعبير المنطقي AND
print("x or y:", x or y)    # التعبير المنطقي OR
print("not x:", not x)      # النفي NOT

# أمثلة إضافية مع Boolean Operators
a, b = 10, 20
print("a > 5 and b < 25:", a > 5 and b < 25)  # شرط مركب باستخدام AND
print("a > 15 or b < 25:", a > 15 or b < 25)  # شرط مركب باستخدام OR
print("not (a > 15):", not (a > 15))         # نفي الشرط