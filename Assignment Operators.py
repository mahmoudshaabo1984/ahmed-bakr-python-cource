# أمثلة على Assignment Operators
print("\nأمثلة على Assignment Operators:")
x = 10
print("Initial x:", x)
x += 5  # إضافة 5 إلى x
print("x += 5:", x)
x -= 3  # طرح 3 من x
print("x -= 3:", x)
x *= 2  # ضرب x في 2
print("x *= 2:", x)
x /= 4  # قسمة x على 4
print("x /= 4:", x)
x %= 3  # باقي قسمة x على 3
print("x %= 3:", x)
x **= 2  # رفع x إلى القوة 2
print("x **= 2:", x)
x //= 2  # القسمة الأرضية x على 2
print("x //= 2:", x)
