# تعريف عدة نصوص تحتوي على أرقام
c, d, f, g = '2', '22', '222', '2222'

# طباعة الأرقام كما هي
print(c)  # النتيجة: "2"
print(d)  # النتيجة: "22"
print(f)  # النتيجة: "222"
print(g)  # النتيجة: "2222"

# استخدام الدالة zfill لإضافة أصفار إلى النصوص لجعل طولها 4 أحرف
print(c.zfill(4))  # النتيجة: "0002" (3 أصفار تمت إضافتها)
print(d.zfill(4))  # النتيجة: "0022" (2 صفرين تمت إضافتهما)
print(f.zfill(4))  # النتيجة: "0222" (صفر واحد تمت إضافته)
print(g.zfill(4))  # النتيجة: "2222" (لا يتم إضافة أصفار لأن الطول 4 بالفعل)
